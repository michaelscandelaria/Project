from project import initializer, cardmarker, cardchecker

def test_initializer():
    assert initializer(0, 12) == True
    assert initializer(0, 19) == False
    assert initializer(1, 25) == True
    assert initializer(1, 32) == False
    assert initializer(2, 40) == True
    assert initializer(2, 29) == False
    assert initializer(3, 55) == True
    assert initializer(3, 44) == False
    assert initializer(4, 61) == True
    assert initializer(4, 75) == True
    assert initializer(4, 78) == False

